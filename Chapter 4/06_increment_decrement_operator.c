#include <stdio.h>

int main(){
    
int i;
i = 5;
 
 printf("The value of i is %d\n", i);
 //5

 printf("The value of i is %d\n", --i);
 //4

printf("The value of i is %d\n", i++);
//4
printf("The value of i is %d\n", i);
//5
printf("The value of i is %d\n", i--);
// 5
printf("The value of i is %d\n", i);
// 4
    return 0;
}